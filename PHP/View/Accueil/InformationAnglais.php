    <h1>Rules</h1>
    <p>Courses and teams are determined before the start of the tournaments, if necessary, adjustments can be made on
        the day of the tournament.

        A game of "Chôlage" is played as follows
        Both teams bet on the number of "chôles" (ie: sets) they are going to play to reach the keg marking the finish
        line, the team who bets the smallest number will be the "chôle" team while the other will be the "déchôle" team.
        Once this is set, the matches may begin, the "chôle" team plays three shots with a large mallet towards the keg,
        then the "déchôle" team plays one shot in whichever direction they want, aiming to drive the ball away.
        If the "chôle" team manages to hit the keg in a number of "chôles" smaller or equal to the number they bet,
        and if the score has been confirmed by both captain, they win the round, and it is the "déchôle" team who pays
        their round and vice versa. </p>
