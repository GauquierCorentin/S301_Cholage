    <h1>Règles</h1>
    <p>Les parcours et les équipes sont déterminées avant le commencement du tournoi, des ajustements peuvent être faits le jour même si nécessaire

        Une partie de chôlage se déroule de la manière suivante :
        Les deux équipes s’affrontant parient sur le nombre de chôles (c’est à dire de manches) qu’elles feront pour atteindre le fût de bière marquant l’arrivée, l’équipe annonçant le plus petit score sera l’équipe qui chôle tandis que l’autre sera l’équipe qui déchôle.
        Une fois cela établi, les manches peuvent commencer, l’équipe chôlante joue trois coups à l’aide d’un maillet en direction du fût puis l’équipe déchôlante joue un coup dans la direction qu’elle veut, son but étant d’éloigner la balle du fût. Si l’équipe chôlante arrive à frapper le fût avec la chôlette (la balle) dans le nombre de chôles imparti, et si le score est validé par les deux capitaines, elle remporte la manche et c’est l’équipe déchôlante qui paie sa tournée et inversement dans le cas contraire. </p>
